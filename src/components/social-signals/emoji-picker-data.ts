export const EMOJI_SOURCE: readonly string[] = Array.from(new Set(EMOJI_SOURCE_RAW));

function sliceRaw(a: number, b: number): string[] {
  return EMOJI_SOURCE_RAW.slice(a, b + 1);
}

function uniq(emojis: string[]): string[] {
  return Array.from(new Set(emojis));
}

function concatRaw(ranges: [number, number][]): string[] {
  const out: string[] = [];
  for (const [a, b] of ranges) {
    out.push(...sliceRaw(a, b));
  }
  return uniq(out);
}

